package br.com.infotravel.api.commonv1.dto;

import br.com.infotravel.api.commonv1.enumerators.DiscountType;

/**
 *
 * @author enioj
 */
import com.fasterxml.jackson.annotation.JsonInclude;

@JsonInclude(JsonInclude.Include.NON_NULL)
public class ApiDiscount {

    private String code;
    private DiscountType type;
    private String description;
    private Double amount;

    public ApiDiscount() {
    }

    public ApiDiscount(String code, DiscountType type, String description, Double amount) {
        this.code = code;
        this.type = type;
        this.description = description;
        this.amount = amount;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public DiscountType getType() {
        return type;
    }

    public void setType(DiscountType type) {
        this.type = type;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Double getAmount() {
        return amount;
    }

    public void setAmount(Double amount) {
        this.amount = amount;
    }

}
